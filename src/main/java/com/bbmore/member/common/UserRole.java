package com.bbmore.member.common;

public enum UserRole {

    ADMIN("ADMIN"),
    MEMBER("MEMBER");

    private String role;

    UserRole(String role) {
        this.role = role;
    }

    public String getRole() {
        return role;
    }

    @Override
    public String toString() {
        return "UserRole{" +
                "role='" + role + '\'' +
                '}';
    }

}
